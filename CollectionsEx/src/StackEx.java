import java.util.Stack;

public class StackEx
{
	public static void main(String[] args)
	{
		
	Stack s=new Stack();
	s.add(10);
	s.add('a');
	s.add("jeevitha");
	s.add(true);
	s.add("Ravi");
	System.out.println(s);
	
	Stack s1=new Stack();
	s1.add(1);
	s1.add('b');
	s1.add("Ravichandran");
	s1.add(false);
	s1.add("jeevi");
	System.out.println(s1);
	
	
	}
}
